package es.caib.helium.integracio.excepcions.unitat;

public class UnitatOrganicaException extends Exception {

	public UnitatOrganicaException() {
		super();
	}

	public UnitatOrganicaException(Throwable cause) {
		super(cause);
	}

	public UnitatOrganicaException(String message) {
		super(message);
	}

	public UnitatOrganicaException(String message, Throwable cause) {
		super(message, cause);
	}
}
