package es.caib.helium.integracio.excepcions.validacio;

public class ValidacioFirmaException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public ValidacioFirmaException() {
		super();
	}

	public ValidacioFirmaException(Throwable cause) {
		super(cause);
	}

	public ValidacioFirmaException(String message) {
		super(message);
	}

	public ValidacioFirmaException(String message, Throwable cause) {
		super(message, cause);
	}

}
