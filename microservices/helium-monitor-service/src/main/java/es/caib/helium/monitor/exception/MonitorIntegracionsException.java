package es.caib.helium.monitor.exception;

public class MonitorIntegracionsException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public MonitorIntegracionsException() {
		super();
	}

	public MonitorIntegracionsException(Throwable cause) {
		super(cause);
	}

	public MonitorIntegracionsException(String message) {
		super(message);
	}

	public MonitorIntegracionsException(String message, Throwable cause) {
		super(message, cause);
	}
}
